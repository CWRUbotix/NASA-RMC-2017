/* this class responsible for the motor speed */
public class Motor {
  private int rmp;        // motor rpm
  private float dist;     // the desired moving distance 
  private float timeout;  // timeout

  /* class constructor */
  public Motor (int r, float d, float t) {
    this.rmp = r;
    this.dist = d;
    this.timeout = t;
  }

  public void setRpm(int r) {
    this.rmp = r;
  }

  public void setDistance(int d) {
    this.dist = d;
  }

  public void setTimeout (float t) {
    this.timeout =t;
  }
}



